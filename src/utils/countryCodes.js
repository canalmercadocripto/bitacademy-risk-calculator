// FunÃ§Ã£o para formatar telefone com cÃ³digo do paÃ­s
export const formatPhoneNumber = (countryCode, phone) => {
  if (!phone) return '';
  return `${countryCode} ${phone}`;
};

// FunÃ§Ã£o para validar nÃºmero de telefone bÃ¡sico
export const validatePhoneNumber = (phone) => {
  // Remove espaÃ§os e caracteres especiais
  const cleanPhone = phone.replace(/[\s\-\(\)\.]/g, '');
  
  // Verifica se contÃ©m apenas nÃºmeros
  if (!/^\d+$/.test(cleanPhone)) {
    return false;
  }
  
  // Verifica se tem pelo menos 8 dÃ­gitos e no mÃ¡ximo 15
  return cleanPhone.length >= 8 && cleanPhone.length <= 15;
};

// FunÃ§Ã£o para buscar paÃ­ses por nome (para pesquisa)
export const searchCountries = (searchTerm) => {
  if (!searchTerm) return countryCodes;
  
  const term = searchTerm.toLowerCase();
  return countryCodes.filter(country => 
    country.name.toLowerCase().includes(term) ||
    country.country.toLowerCase().includes(term) ||
    country.code.includes(term)
  );
};

